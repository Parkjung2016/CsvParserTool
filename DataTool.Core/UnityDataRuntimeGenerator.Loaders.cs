            sb.AppendLine("                throw new ArgumentNullException(nameof(global));");
            sb.AppendLine();
            sb.AppendLine("            var properties = new List<PropertyInfo>();");
            sb.AppendLine("            foreach (PropertyInfo prop in typeof(GlobalDataContainer).GetProperties(BindingFlags.Public | BindingFlags.Instance | BindingFlags.DeclaredOnly))");
            sb.AppendLine("            {");
            sb.AppendLine("                if (!prop.CanRead || !IsDataContainerProperty(prop.PropertyType))");
            sb.AppendLine("                    continue;");
            sb.AppendLine("                if (prop.PropertyType.GetMethod(\"Load\", Type.EmptyTypes) != null)");
            sb.AppendLine("                    properties.Add(prop);");
            sb.AppendLine("            }");
            sb.AppendLine();
            sb.AppendLine("            var watch = System.Diagnostics.Stopwatch.StartNew();");
            sb.AppendLine("            int total = properties.Count;");
            sb.AppendLine("            int completed = 0;");
            sb.AppendLine("            progress?.Invoke(new DataLoadProgressInfo(DataLoadState.Started, string.Empty, completed, total, watch.ElapsedMilliseconds));");
            sb.AppendLine("            foreach (PropertyInfo prop in properties)");
            sb.AppendLine("            {");
            sb.AppendLine("                progress?.Invoke(new DataLoadProgressInfo(DataLoadState.Loading, prop.Name, completed, total, watch.ElapsedMilliseconds));");
            sb.AppendLine("                try");
            sb.AppendLine("                {");
            sb.AppendLine("                    object container = prop.GetValue(global);");
            sb.AppendLine("                    MethodInfo load = prop.PropertyType.GetMethod(\"Load\", Type.EmptyTypes);");
            sb.AppendLine("                    load.Invoke(container, null);");
            sb.AppendLine("                }");
            sb.AppendLine("                catch (Exception ex)");
            sb.AppendLine("                {");
            sb.AppendLine("                    Exception error = ex is TargetInvocationException invocation && invocation.InnerException != null ? invocation.InnerException : ex;");
            sb.AppendLine("                    progress?.Invoke(new DataLoadProgressInfo(DataLoadState.Failed, prop.Name, completed, total, watch.ElapsedMilliseconds, error.Message));");
            sb.AppendLine("                    throw error;");
            sb.AppendLine("                }");
            sb.AppendLine("                completed++;");
            sb.AppendLine("                progress?.Invoke(new DataLoadProgressInfo(DataLoadState.Loaded, prop.Name, completed, total, watch.ElapsedMilliseconds));");
            sb.AppendLine("            }");
            sb.AppendLine("            progress?.Invoke(new DataLoadProgressInfo(DataLoadState.Completed, string.Empty, completed, total, watch.ElapsedMilliseconds));");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        public static void LoadAllContainers() => LoadAllContainers(GlobalDataContainer.Instance, null);");
            sb.AppendLine("        public static void LoadAllContainers(Action<DataLoadProgressInfo> progress) => LoadAllContainers(GlobalDataContainer.Instance, progress);");
            sb.AppendLine();
            sb.AppendLine("        public static void Load<T>(DataContainerBase<T> container)");
            sb.AppendLine("            where T : class");
            sb.AppendLine("        {");
            sb.AppendLine("            string name = typeof(T).Name;");
            sb.AppendLine("#if UNITY_EDITOR");
            sb.AppendLine("            if (!Application.isPlaying)");
            sb.AppendLine("            {");
            sb.AppendLine("                LoadFromEditorFiles(container, name);");
            sb.AppendLine("                return;");
            sb.AppendLine("            }");
            sb.AppendLine("#endif");
            sb.AppendLine("            if (!IsUseBytesFile())");
            sb.AppendLine("            {");
            sb.AppendLine("                string csvKey = CsvAddressKey(name);");
            sb.AppendLine("                AsyncOperationHandle<TextAsset> csvHandle = Addressables.LoadAssetAsync<TextAsset>(csvKey);");
            sb.AppendLine("                try");
            sb.AppendLine("                {");
            sb.AppendLine("                    TextAsset csvTa = WaitForHandle(csvHandle);");
            sb.AppendLine("                    if (csvTa == null || string.IsNullOrEmpty(csvTa.text))");
            sb.AppendLine("                        throw new InvalidOperationException($\"CSV not found or empty: {csvKey} ({name})\");");
            sb.AppendLine();
            sb.AppendLine("                    List<T> csvList = ParseCsvText<T>(csvTa.text);");
            sb.AppendLine("                    container.ReplaceFromLoader(csvList);");
            sb.AppendLine("                    return;");
            sb.AppendLine("                }");
            sb.AppendLine("                finally");
            sb.AppendLine("                {");
            sb.AppendLine("                    Addressables.Release(csvHandle);");
            sb.AppendLine("                }");
            sb.AppendLine("            }");
            sb.AppendLine();
            sb.AppendLine("            string byteKey = ByteAddressKey(name);");
            sb.AppendLine("            AsyncOperationHandle<TextAsset> byteHandle = Addressables.LoadAssetAsync<TextAsset>(byteKey);");
            sb.AppendLine("            try");
            sb.AppendLine("            {");
            sb.AppendLine("                TextAsset byteTa = WaitForHandle(byteHandle);");
            sb.AppendLine("                if (byteTa == null)");
            sb.AppendLine("                    throw new InvalidOperationException(\"Addressables TextAsset not found: \" + byteKey);");
            sb.AppendLine("                byte[] raw = byteTa.bytes;");
            sb.AppendLine("                List<T> list = MessagePackSerializer.Deserialize<List<T>>(raw, SheetOptions);");
            sb.AppendLine("                container.ReplaceFromLoader(list ?? new List<T>());");
            sb.AppendLine("            }");
            sb.AppendLine("            finally");
            sb.AppendLine("            {");
            sb.AppendLine("                Addressables.Release(byteHandle);");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        private static List<T> ParseCsvText<T>(string text)");
            sb.AppendLine("            where T : class");
            sb.AppendLine("        {");
            sb.AppendLine("            using var reader = new StringReader(StripToolMetadataRows(text));");
            sb.AppendLine("            using var csv = new CsvReader(reader, CultureInfo.InvariantCulture);");
            sb.AppendLine("            var rows = new List<T>();");
            sb.AppendLine("            foreach (T row in csv.GetRecords<T>())");
            sb.AppendLine("                rows.Add(row);");
            sb.AppendLine("            return rows;");
            sb.AppendLine("        }");
            sb.AppendLine(EndIf);
            sb.AppendLine("    }");
            sb.AppendLine("}");
            return sb.ToString();
        }

